problem3a(Power) :-
    data(L), tally(L, Counts), countbits(Counts, Gamma, Epsilon), Power is Gamma*Epsilon.

countbits(Counts, MSB, LSB) :-
    countbits(Counts, 0, 0, MSB, LSB).

countbits([], MSB, LSB, MSB, LSB).
countbits([(C0,C1)|T], M, L, MSB, LSB) :-
    C0 > C1,
    M1 is M*2,			% most common bit is 0
    L1 is L*2 + 1,		% least common bit is 1
    countbits(T, M1, L1, MSB, LSB).
countbits([(C0,C1)|T], M, L, MSB, LSB) :-
    C0 =< C1,
    M1 is M*2 + 1,		% most common bit is 1
    L1 is L*2,			% least common bit is 0
    countbits(T, M1, L1, MSB, LSB).

% tally([[0,0,1,0,0], ...], [(C0,C1), ... ] - produce a tally of 0:s and 1:s
tally([X], C) :-
    count(X, C).
tally([H|T], C) :-
    count(H, C1),
    tally(T, C2),
    add(C1, C2, C).

% count([0,0,0,1,0], [(1,0),(1,0),(1,0),(0,1),(1,0)])
% count numbers of 0:s and 1:s in list
count([], []).
count([0|Bits], [(1,0)|T]) :-
    count(Bits, T).
count([1|Bits], [(0,1)|T]) :-
    count(Bits, T).

% add two lists of counts
add([], [], []).
add([(X0,X1)|Xs], [(Y0,Y1)|Ys], [(Z0,Z1)|Zs]) :-
    Z0 is X0+Y0,
    Z1 is X1+Y1,
    add(Xs, Ys, Zs).

